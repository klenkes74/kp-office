package de.kaiserpfalzEdv.office.security;

import org.springframework.data.domain.Page;
import org.springframework.data.domain.Pageable;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.PagingAndSortingRepository;
import org.springframework.data.repository.query.Param;
import org.springframework.stereotype.Repository;

import java.util.UUID;

/**
 * @author klenkes &lt;rlichti@kaiserpfalz-edv.de&gt;
 * @version 0.1.0
 * @since 0.1.0
 */
@Repository
public interface AccountRepository extends PagingAndSortingRepository<Account, UUID> {
    public Page<Account> findByDisplayName(String displayName, Pageable page);
    public Page<Account> findByDisplayNumber(String displayNumber, Pageable page);

    @Query("select a from Account a where displayNumber=:accountName")
    public Account findByAccountName(@Param("accountName") String accountName);
}

package de.kaiserpfalzEdv.office.ui.web.mainScreen;

import org.apache.commons.lang3.builder.EqualsBuilder;
import org.apache.commons.lang3.builder.HashCodeBuilder;
import org.apache.commons.lang3.builder.ToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

import java.util.UUID;

/**
 * @author klenkes
 * @version 2015Q1
 * @since 18.02.15 07:10
 */
public class AbstractMainTabEvent {
    private UUID id;
    private UUID tabId;


    public AbstractMainTabEvent(final UUID eventId, final UUID tabId) {
        this.id = eventId;
        this.tabId = tabId;
    }


    public UUID getId() {
        return id;
    }


    public UUID getTabId() {
        return tabId;
    }


    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (obj == this) {
            return true;
        }
        if (!AbstractMainTabEvent.class.isAssignableFrom(obj.getClass())) {
            return false;
        }

        AbstractMainTabEvent rhs = (AbstractMainTabEvent) obj;
        return new EqualsBuilder()
                .append(this.id, rhs.id)
                .isEquals();
    }

    @Override
    public int hashCode() {
        return new HashCodeBuilder()
                .append(id)
                .toHashCode();
    }

    @Override
    public String toString() {
        return new ToStringBuilder(this, ToStringStyle.SHORT_PREFIX_STYLE)
                .append("id", id)
                .append("tab", tabId)
                .toString();
    }
}

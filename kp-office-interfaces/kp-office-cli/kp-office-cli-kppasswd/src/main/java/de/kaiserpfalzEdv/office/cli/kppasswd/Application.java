package de.kaiserpfalzEdv.office.cli.kppasswd;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import javax.crypto.SecretKeyFactory;
import javax.crypto.spec.PBEKeySpec;
import java.security.NoSuchAlgorithmException;
import java.security.SecureRandom;
import java.security.spec.InvalidKeySpecException;
import java.security.spec.KeySpec;
import java.util.Base64;

/**
 * @author klenkes &lt;rlichti@kaiserpfalz-edv.de&gt;
 * @version 0.1.0
 * @since 0.1.0
 */
public class Application {
    private static final Logger LOG = LoggerFactory.getLogger(Application.class);


    public static void main(String[] args) throws NoSuchAlgorithmException, InvalidKeySpecException {
        Application app = new Application();
        Base64.Encoder encoder = Base64.getEncoder();

        byte[] salt = app.generateSalt();

        System.out.println("Input: " + args[0]
                + " Output: " + encoder.encodeToString(app.encrypt(args[0], salt))
                + ", salt: " + encoder.encodeToString(salt));
    }


    private byte[] encrypt(final String password, final byte[] salt) throws NoSuchAlgorithmException, InvalidKeySpecException {
        String algorithm = "PBKDF2WithHmacSHA1";
        int derivedKeyLength = 160;
        int iterations = 20000;

        KeySpec spec = new PBEKeySpec(password.toCharArray(), salt, iterations, derivedKeyLength);

        SecretKeyFactory f = SecretKeyFactory.getInstance(algorithm);

        return f.generateSecret(spec).getEncoded();
    }

    private byte[] generateSalt() throws NoSuchAlgorithmException {
        SecureRandom random = SecureRandom.getInstance("SHA1PRNG");

        // Generate a 8 byte (64 bit) salt as recommended by RSA PKCS5
        byte[] salt = new byte[8];
        random.nextBytes(salt);

        return salt;
    }
}

package de.kaiserpfalzEdv.office.cli.executor.events;

import org.apache.commons.lang3.builder.EqualsBuilder;
import org.apache.commons.lang3.builder.HashCodeBuilder;
import org.apache.commons.lang3.builder.ToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import java.util.EventObject;
import java.util.UUID;

/**
 * @author klenkes
 * @version 2015Q1
 * @since 05.03.15 09:03
 */
public class AbstractEventingBase extends EventObject {
    private static final Logger LOG = LoggerFactory.getLogger(AbstractEventingBase.class);
    protected final UUID id;

    public AbstractEventingBase(Object source, final UUID id) {
        super(source);

        this.id = id;
    }


    public UUID getId() {
        return id;
    }


    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }
        if (obj == this) {
            return true;
        }
        if (obj.getClass() != getClass()) {
            return false;
        }
        AbstractBaseEvent rhs = (AbstractBaseEvent) obj;
        return new EqualsBuilder()
                .append(this.id, rhs.id)
                .isEquals();
    }

    @Override
    public int hashCode() {
        return new HashCodeBuilder()
                .append(id)
                .toHashCode();
    }

    @Override
    public String toString() {
        return new ToStringBuilder(this, ToStringStyle.SHORT_PREFIX_STYLE)
                .append("id", id)
                .toString();
    }
}
